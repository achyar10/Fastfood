    private void tabel_transaksi(){
        model = new DefaultTableModel();
        table_transaksi.setModel(model);
        model.addColumn("Id");
        model.addColumn("No Transaksi");
        model.addColumn("Paket");
        model.addColumn("Harga");
        model.addColumn("Jumlah");
        model.addColumn("Total");
    }
    
    private void tampil_tabel(){
       Connection con = koneksi.getConnection();
       try {
           stt = con.createStatement();
           String sql = "SELECT * FROM paket";
           rss = stt.executeQuery(sql);
           while(rss.next()){
               Object[] data;
               data = new Object[4];
               data[0] = rss.getString("paket_kode");
               data[1] = rss.getString("paket_nama");
               data[2] = rss.getString("paket_keterangan");
               data[3] = rss.getString("paket_harga");
               model.addRow(data);
           }
       } catch (Exception e) {
           e.printStackTrace();
       }
   }
    
    private void tampil_tabel_transaksi(){
       Connection con = koneksi.getConnection();
       try {
           stt = con.createStatement();
           String sql = "SELECT * FROM detil_transaksi WHERE transaksi_id = '"+txt_no_trx.getText()+"'";
           rss = stt.executeQuery(sql);
           while(rss.next()){
               Object[] data;
               data = new Object[6];
               data[0] = rss.getString(1);
               data[1] = rss.getString(2);
               data[2] = rss.getString(3);
               data[3] = rss.getString(4);
               data[4] = rss.getString(5);
               data[5] = rss.getString(6);
               model.addRow(data);
           }
       } catch (Exception e) {
           e.printStackTrace();
       }
   }
    
    
