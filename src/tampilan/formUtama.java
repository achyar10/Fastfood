    private void btn_beliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_beliActionPerformed
       Connection con = koneksi.getConnection();
        if(txt_jumlah.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Data tidak boleh kosong");
        } else {
            int total = Integer.parseInt(txt_harga_paket.getText()) * Integer.parseInt(txt_jumlah.getText());
            int simpan2 = 0;
            if(txt_total.getText().equals("")){
                simpan2 = 0;
            }
            else
                simpan2 = Integer.valueOf(txt_total.getText());

            String sql2 = "INSERT INTO detil_transaksi(transaksi_id, paket_kode, detil_transaksi_harga, detil_transaksi_jumlah, detil_transaksi_total) VALUES('"+txt_no_trx.getText()+"','"+combo_paket.getSelectedItem().toString()+"','"+txt_harga_paket.getText()+"','"+txt_jumlah.getText()+"','"+total+"')";
            try {
                    simpan_no();
                    stt2 = con.createStatement();
                    stt2.executeUpdate(sql2);
                    txt_total.setText(String.valueOf(simpan2+total));
                    tabel_transaksi();
                    tampil_tabel_transaksi();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_beliActionPerformed

    private void btn_hapus_trxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapus_trxActionPerformed
        Connection con = koneksi.getConnection();
        int row = table_transaksi.getSelectedRow();
        try {
            stt = con.createStatement();
            if(row == -1){
                JOptionPane.showMessageDialog(this, "Anda Belum Memilih Tabel");
            }
            else{
                int harga = Integer.parseInt((String) table_transaksi.getModel().getValueAt(row, 5));
                int total = Integer.parseInt(txt_total.getText());
                String sql = "DELETE FROM detil_transaksi WHERE detil_transaksi_id = '"+table_transaksi.getModel().getValueAt(row, 0)+"'";
                stt.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Berhasil Dihapus");
                txt_total.setText(String.valueOf(total-harga));
                tabel_transaksi();
                tampil_tabel_transaksi();
                clear();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_hapus_trxActionPerformed

