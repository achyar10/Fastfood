    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        Connection con = koneksi.getConnection();
        try {
         if(btn_save.getText().equals("Batal")){
             clear();
             btn_save.setText("Simpan");
         }
         else{
            if(txt_kode.getText().equals("")||txt_makan.getText().equals("")||txt_area_ket.getText().equals("")||txt_harga.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Mohon diisi data yang kosong", "Pesan", JOptionPane.WARNING_MESSAGE);
            }
            else{    
            stt = con.createStatement();
            String sql = "INSERT INTO paket VALUES('"+txt_kode.getText()+"', '"+txt_makan.getText()+"','"+txt_area_ket.getText()+"','"+txt_harga.getText()+"')";   
            stt.executeUpdate(sql);
            JOptionPane.showMessageDialog(rootPane, "Data berhasil disimpan");
            tabel_makanan();
            tampil_tabel();
            clear();
           }
         }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal");
        }
    }//GEN-LAST:event_btn_saveActionPerformed

